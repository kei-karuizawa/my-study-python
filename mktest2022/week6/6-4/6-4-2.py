# if 语句

i = 2
if i == 1:
    print('条件为真执行这里')
print('if 条件语句结束')


i = 1
if i == 1:
    print('条件为真执行这里')
else:
    print('条件为假执行这里')

print('if 条件语句结束')

i = 2
if i == 1:
    print('i 为 1 执行这里')
elif i == 2:
    print('i 为 2 执行这里')
elif i == 3:
    print('i 为 3 执行这里')
else:
    print('i 不为 1、2、3 执行这里')

print('if 条件语句结束')
