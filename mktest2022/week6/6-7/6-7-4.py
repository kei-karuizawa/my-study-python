# 字典的声明与访问

d = {
    'a': '1',  # 冒号前边的部分，我们叫做字典的 key 键
    'b': '2'  # 冒号后边的部分，我们叫做字典的 value 值
}
print(d)  # {'a': '1', 'b': '2'}

# 访问字典所有的键
print(d.keys())  # dict_keys(['a', 'b'])
# 访问字典所有的值
print(d.values())  # dict_values(['1', '2'])

# 根据字典的键访问对应的值
print(d['b'])  # 2
print(d['c'])  # 报错；若键在字典中不存在，访问会报错
